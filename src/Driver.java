public class Driver {

    String name;        //instance variable for Driver: name
    int age;        //instance variable for Driver: age

    //constructor for Driver
    public Driver(String name, int age) {
        this.name = name;
        this.age = age;
    }
    //getter for Driver: name
    public String getName() {
        return name;
    }
    //setter for Driver: name
    public void setName(String name) {
        this.name = name;
    }
    //getter for Driver: age
    public int getAge() {
        return age;
    }
    //setter for Driver: age
    public void setAge(int age) {
        this.age = age;
    }
}


